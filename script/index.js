const createElements = (arr) =>{
    htmlElements = arr.map((el) => `<span class="btn">${el}</span>`);
    return(htmlElements.join(" "));
}

function pronounceWord(word) {
    const utterance = new SpeechSynthesisUtterance(word);
    utterance.lang = "en-EN"; // English
    window.speechSynthesis.speak(utterance);
}

const manageSpinner = (status) =>{
    if(status === true){
        document.getElementById("spinner").classList.remove("hidden")
        document.getElementById("word-container").classList.add("hidden")
    }
    else{
        document.getElementById("word-container").classList.remove("hidden")
        document.getElementById("spinner").classList.add("hidden")
    }
}

const loadLessons = () => {
    fetch("https://openapi.programming-hero.com/api/levels/all")
        .then((res) => res.json())
        .then((data) => displayLessons(data.data))
};

const removeActive = () => {
    const lessonButtons = document.querySelectorAll(".lesson-btn");
    lessonButtons.forEach((btn) => btn.classList.remove("active"));
};

const loadLevelWord = (id) => {
    manageSpinner(true);

    const url = `https://openapi.programming-hero.com/api/level/${id}`
    fetch(url)
        .then((res) => res.json())
        .then((data) => {

            removeActive();  // remove all active classes

            const clickBtn = document.getElementById(`lesson-btn-${id}`);
            // console.log(clickBtn)
            clickBtn.classList.add("active");   // add active class

            displayLevelWords(data.data)
        })

}

const loadWordDetail = async(id)=>{
    const url = `https://openapi.programming-hero.com/api/word/${id}`;
    const res = await fetch(url);
    const details = await res.json()
    displayWordDetails(details.data)
}

const displayWordDetails = (word)=>{
    // console.log(word)
    const detailsBox = document.getElementById("details-container");
    detailsBox.innerHTML = `
        <div class="">
            <h2 class="text-2xl font-bold">${word.word} (<i class="fa-solid fa-microphone-lines"></i> :${word.pronunciation})
            </h2>
        </div>                
        <div class="">
            <h2 class="text-2xl font-bold">Meaning</h2>
            <p>${word.meaning}</p>
        </div>                
        <div class="">
            <h2 class="text-2xl font-bold">Example </h2>
            <p>${word.sentence}</p>
        </div>                
        <div class="">
            <h2 class="text-2xl font-bold">Synonym</h2>
            <div class = "">${createElements(word.synonyms)}</div>
        </div>
    `;

    document.getElementById("word_modal").showModal()
}

const displayLevelWords = (words) => {

    const wordContainer = document.getElementById("word-container");
    wordContainer.innerHTML = ""

    if (words.length == 0) {

        wordContainer.innerHTML = `
            <div class=" text-center col-span-full py-5 rounded-lg space-y-4">
                <img class="mx-auto" src="./assets/alert-error.png" alt="">
                <p class="font-bangla text-xl font-medium text-gray-400">এই Lesson-এ কোনো Vocabulary যুক্ত করা হয়নি।</p>
                <h2 class="font-bangla text-2xl font-semibold text-gray-700">পরবর্তী Lesson-এ যান...</h2>
            </div>
        `;
        manageSpinner(false);
        return
    }

    words.forEach((word) => {

        const card = document.createElement("div");
        card.innerHTML = `
            <div class="bg-white rounded-xl shadow-sm text-center py-10 px-5 space-y-3">
                <h2 class="font-bold text-2xl">${word.word ? word.word : "শব্দটি খুঁজে পাওয়া যায়নি"}</h2>
                <p class="font-semibold">"${word.meaning ? word.meaning : "অর্থ খুঁজে পাওয়া যায়নি"} / ${word.pronunciation ? word.pronunciation : "Pronunciation পাওয়া যায়নি"}"</p>
                <div class="font-bangla text-2xl font-medium"></div>
                <div class="flex justify-between items-center">
                    <button onclick="loadWordDetail(${word.id})" class="btn bg-sky-50 hover:bg-sky-200"><i class="fa-solid fa-circle-info"></i>
                    </button>
                    <button onclick = "pronounceWord('${word.word}')" class="btn bg-sky-50 hover:bg-sky-200"><i class="fa-solid fa-volume-high"></i></button>
                </div>
            </div>
        `;

        wordContainer.append(card)
    });
    manageSpinner(false);
}

const displayLessons = (lessons) => {
    // 1. get the container and make initially empty
    const levelContainer = document.getElementById("level-container");
    levelContainer.innerHTML = "";

    // 2. get into every lessons
    for (let lesson of lessons) {

        // 3. create element
        const btnDiv = document.createElement("div");
        btnDiv.innerHTML = `
            <button id="lesson-btn-${lesson.level_no}" onclick = loadLevelWord(${lesson.level_no}) class="btn btn-outline btn-primary lesson-btn">
                <i class="fa-solid fa-book-open"></i> Lesson - ${lesson.level_no}
            </button>
        `;

        // 4. append the element into container
        levelContainer.append(btnDiv)

    }

}

loadLessons()

document.getElementById("btn-search").addEventListener("click", ()=>{

    removeActive()
    const input = document.getElementById("input-search");
    const searchValue = input.value.trim().toLowerCase();

    fetch("https://openapi.programming-hero.com/api/words/all")
    .then(res => res.json())
    .then(data =>{
        const allWords = data.data; 
        const filterWords = allWords.filter(word => word.word.toLowerCase().includes(searchValue))

        displayLevelWords(filterWords);
    })

});